



const day = 1;

switch(day){
    case 0:
    document.write("monday")
    break;

    case 1:
    document.write("tuesday")
    break;

    case 2:
    document.write("wednesday");
    break;

    case 3:
    document.write("thrusday");
    break;

    case 4:
    document.write("friday");
    break;

    case 5:
    document.write("saturday");
    break;

    default:
    document.write("sunday");


}